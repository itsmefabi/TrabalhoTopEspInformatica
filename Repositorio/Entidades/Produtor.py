class Produtor:
    id_produtor = None
    nome = None

    def getIdProdutor(self):
        return self.id_produtor    
    def setIdProdutor(self, id_produtor):
        self.id_produtor = id_produtor

    def getNome(self):
        return self.nome    
    def setNome(self, nome):
        self.nome = nome
